function [points]=LetterLibrary(str)
points.X=0;
points.Y=0;

if str=='U'
    points.X=[0 0 0 0.1/3 0.1 0.2-.1/3 0.2 0.2 0.2];
    points.Y=[0 -0.1 -0.2 -0.2-.2/3 -0.3 -0.3+.1/3 -0.2 -0.1 0];
elseif str=='S'
    points.X=[];
    points.Y=[];
    points.X=[0.2 0.05 0.02 0.02 0.1/3 0.1 0.1+0.2/3 0.18 ...
        0.18 0.15 0];
    points.Y=[0 0 -0.05 -0.1 -0.1-.1/3 -0.15 -0.2+.1/3 ...
        -0.2 -0.25 -0.3 -0.3];
elseif str=='C'
    points.X=([0.16 0.1+.1/3 0.2/3 0.1/3 ...
        0.02 ...
        0.1/3 0.2/3 0.1+0.1/3 0.16] -0.02) * 0.2/0.14;
    points.Y=([-0.24 -0.28 -0.28 -0.23 ...
        -0.15 ...
        -0.07 -0.02 -0.02 -0.06]+0.02)*0.3/0.26 ;
end

end
